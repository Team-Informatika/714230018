package main

// EqualInts returns true if a equals b
func EqualInts(a, b int) bool {
	return a == b
}

// NotEqualInts returns true if a does not equal b
func NotEqualInts(a, b int) bool {
	return a != b
}

// GreaterThanInts returns true if a is greater than b
func GreaterThanInts(a, b int) bool {
	return a > b
}

// LessThanInts returns true if a is less than b
func LessThanInts(a, b int) bool {
	return a < b
}

// GreaterThanOrEqualInts returns true if a is greater than or equal to b
func GreaterThanOrEqualInts(a, b int) bool {
	return a >= b
}

// LessThanOrEqualInts returns true if a is less than or equal to b
func LessThanOrEqualInts(a, b int) bool {
	return a <= b
}

